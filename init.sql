-- Función para reiniciar únicamente las ventas y reiniciar los contadores e IDs secuenciales (TRUNCATE ... RESTART IDENTITY CASCADE)
CREATE OR REPLACE FUNCTION reset_sales_and_sequences()
RETURNS text AS $$
BEGIN
    TRUNCATE TABLE sale_items RESTART IDENTITY CASCADE;
    TRUNCATE TABLE sales RESTART IDENTITY CASCADE;
    
    -- Reiniciar secuencias explícitamente
    -- BIGSERIAL crea una secuencia por defecto. Buscamos el nombre de la secuencia asociada a 'id' en 'sale_items'
    EXECUTE 'ALTER SEQUENCE IF EXISTS ' || pg_get_serial_sequence('public.sale_items', 'id') || ' RESTART WITH 1';
    
    -- Reiniciar secuencia de orden secuencial
    ALTER SEQUENCE IF EXISTS sales_numero_orden_seq RESTART WITH 1;
    
    RETURN 'Ventas y detalles eliminados con éxito, secuencias de IDs reiniciadas.';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


