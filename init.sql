-- E. RPC: Descuento Atómico de Stock (Evita inconsistencias / condiciones de carrera)
CREATE OR REPLACE FUNCTION deduct_stock_atomic(items_to_deduct jsonb)
RETURNS jsonb AS $$
DECLARE
  item record;
  p_stock int;
  p_nombre text;
  result jsonb := '{"success": true}';
BEGIN
  -- 1. Verificar disponibilidad de stock para cada elemento antes de realizar cualquier cambio
  FOR item IN SELECT * FROM jsonb_to_recordset(items_to_deduct) AS (id text, cantidad int) LOOP
    SELECT stock, nombre INTO p_stock, p_nombre FROM products WHERE id = item.id FOR UPDATE;
    IF NOT FOUND THEN
      RETURN jsonb_build_object('success', false, 'error', 'Sabor de helado no encontrado: ' || item.id);
    END IF;
    IF p_stock < item.cantidad THEN
      RETURN jsonb_build_object('success', false, 'error', 'Stock insuficiente para ' || p_nombre, 'nombre', p_nombre, 'stock', p_stock, 'requested', item.cantidad);
    END IF;
  END LOOP;

  -- 2. Restar stock a los productos correspondientes
  FOR item IN SELECT * FROM jsonb_to_recordset(items_to_deduct) AS (id text, cantidad int) LOOP
    UPDATE products SET stock = stock - item.cantidad, updated_at = NOW() WHERE id = item.id;
  END LOOP;

  RETURN result;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


-- F. RPC: Restauración Atómica de Stock (Devolución por cancelación o rechazo de ventas)
CREATE OR REPLACE FUNCTION restore_stock_atomic(items_to_restore jsonb)
RETURNS jsonb AS $$
DECLARE
  item record;
  result jsonb := '{"success": true}';
BEGIN
  FOR item IN SELECT * FROM jsonb_to_recordset(items_to_restore) AS (id text, cantidad int) LOOP
    UPDATE products SET stock = stock + item.cantidad, updated_at = NOW() WHERE id = item.id;
  END LOOP;
  RETURN result;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


-- G. RPC: Obtener Estadísticas y KPIs Globales Rápidos (Mejora drásticamente el rendimiento de la App)
CREATE OR REPLACE FUNCTION get_shop_kpis(p_fecha_inicio text DEFAULT NULL, p_fecha_fin text DEFAULT NULL)
RETURNS jsonb AS $$
DECLARE
  v_ventas_totales numeric := 0;
  v_costos_totales numeric := 0;
  v_ganancia_neta numeric := 0;
  v_num_pedidos int := 0;
  v_ticket_promedio numeric := 0;
  v_metodo_favorito text := 'Ninguno';
BEGIN
  -- Filtrar según las fechas si se suministran, de lo contrario tomar el histórico total
  SELECT 
    COALESCE(SUM(total), 0),
    COUNT(*),
    COALESCE(AVG(total), 0)
  INTO v_ventas_totales, v_num_pedidos, v_ticket_promedio
  FROM sales
  WHERE (p_fecha_inicio IS NULL OR fecha >= p_fecha_inicio)
    AND (p_fecha_fin IS NULL OR fecha <= p_fecha_fin)
    AND estado NOT IN ('Rechazado', 'Eliminada');

  -- Calcular costos totales recorriendo los items de ventas válidas
  SELECT COALESCE(SUM(i.cantidad * i.costo_unitario), 0)
  INTO v_costos_totales
  FROM sale_items i
  JOIN sales s ON i.sale_id = s.id
  WHERE (p_fecha_inicio IS NULL OR s.fecha >= p_fecha_inicio)
    AND (p_fecha_fin IS NULL OR s.fecha <= p_fecha_fin)
    AND s.estado NOT IN ('Rechazado', 'Eliminada');

  v_ganancia_neta := v_ventas_totales - v_costos_totales;

  -- Método de pago más utilizado
  SELECT payment_method
  INTO v_metodo_favorito
  FROM sales
  WHERE (p_fecha_inicio IS NULL OR fecha >= p_fecha_inicio)
    AND (p_fecha_fin IS NULL OR fecha <= p_fecha_fin)
    AND estado NOT IN ('Rechazado', 'Eliminada')
  GROUP BY payment_method
  ORDER BY COUNT(*) DESC
  LIMIT 1;

  RETURN jsonb_build_object(
    'ventas_totales', v_ventas_totales,
    'costos_totales', v_costos_totales,
    'ganancia_neta', v_ganancia_neta,
    'num_pedidos', v_num_pedidos,
    'ticket_promedio', ROUND(v_ticket_promedio, 2),
    'metodo_preferido', COALESCE(v_metodo_favorito, 'N/A')
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


-- H. RPC: Agrupación de Ingresos de los Últimos Días (Gráfica de Tendencias en la Base de Datos)
CREATE OR REPLACE FUNCTION get_revenue_by_day(p_limit int DEFAULT 30)
RETURNS TABLE (fecha_dia TEXT, total_ventas NUMERIC, total_pedidos BIGINT) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    fecha AS fecha_dia,
    COALESCE(SUM(total), 0) AS total_ventas,
    COUNT(*) AS total_pedidos
  FROM sales
  WHERE estado NOT IN ('Rechazado', 'Eliminada')
  GROUP BY fecha
  ORDER BY fecha DESC
  LIMIT p_limit;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


