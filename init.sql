-- B. Función de Auditoría de Productos
CREATE OR REPLACE FUNCTION process_audit_products()
RETURNS TRIGGER AS $$
DECLARE
    action_type TEXT;
    val_old JSONB := NULL;
    val_new JSONB := NULL;
    responsable TEXT := 'sistema_db';
BEGIN
    IF (TG_OP = 'DELETE') THEN 
        action_type := 'eliminacion_producto';
        val_old := to_jsonb(OLD);
    ELSIF (TG_OP = 'INSERT') THEN 
        action_type := 'creacion_producto';
        val_new := to_jsonb(NEW);
    ELSIF (TG_OP = 'UPDATE') THEN
        action_type := 'actualizacion_producto';
        val_old := to_jsonb(OLD);
        val_new := to_jsonb(NEW);
        IF (OLD.stock <> NEW.stock) THEN 
            action_type := 'cambio_stock';
        ELSIF (OLD.precio <> NEW.precio) THEN 
            action_type := 'cambio_precio';
        END IF;
    END IF;

    INSERT INTO audit_logs (fecha, hora, accion, admin_responsable, entidad_afectada, entidad_id, valor_anterior, valor_nuevo)
    VALUES (
        to_char(NOW() AT TIME ZONE 'America/Bogota', 'YYYY-MM-DD'), 
        to_char(NOW() AT TIME ZONE 'America/Bogota', 'HH24:MI'), 
        action_type, 
        responsable, 
        'products', 
        COALESCE(NEW.id, OLD.id), 
        val_old, 
        val_new
    );
    
    IF (TG_OP = 'DELETE') THEN RETURN OLD; ELSE RETURN NEW; END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER trg_audit_products AFTER INSERT OR UPDATE OR DELETE ON products FOR EACH ROW EXECUTE FUNCTION process_audit_products();


-- C. Función de Auditoría de Ventas y Sincronización de Pago
CREATE OR REPLACE FUNCTION process_audit_sales()
RETURNS TRIGGER AS $$
DECLARE
    action_type TEXT;
    val_old JSONB := NULL;
    val_new JSONB := NULL;
    responsable TEXT := 'sistema_ventas';
BEGIN
    IF (TG_OP = 'DELETE') THEN 
        action_type := 'eliminacion_venta';
        val_old := to_jsonb(OLD);
    ELSIF (TG_OP = 'INSERT') THEN 
        action_type := 'creacion_venta';
        val_new := to_jsonb(NEW);
    ELSIF (TG_OP = 'UPDATE') THEN
        action_type := 'actualizacion_venta';
        val_old := to_jsonb(OLD);
        val_new := to_jsonb(NEW);

        -- Lógica de negocio: Si se anula la venta, se anula el pago automáticamente
        IF (NEW.estado IN ('Rechazado', 'Eliminada')) THEN
            NEW.payment_status := 'Anulado';
        END IF;

        IF (OLD.estado <> NEW.estado) THEN 
            action_type := 'cambio_estado_venta';
        END IF;
    END IF;

    INSERT INTO audit_logs (fecha, hora, accion, admin_responsable, entidad_afectada, entidad_id, valor_anterior, valor_nuevo)
    VALUES (
        to_char(NOW() AT TIME ZONE 'America/Bogota', 'YYYY-MM-DD'), 
        to_char(NOW() AT TIME ZONE 'America/Bogota', 'HH24:MI'), 
        action_type, 
        responsable, 
        'sales', 
        COALESCE(NEW.id, OLD.id), 
        val_old, 
        val_new
    );
    
    IF (TG_OP = 'DELETE') THEN RETURN OLD; ELSE RETURN NEW; END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER trg_audit_sales AFTER INSERT OR UPDATE OR DELETE ON sales FOR EACH ROW EXECUTE FUNCTION process_audit_sales();


